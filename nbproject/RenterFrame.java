    private void showTable(){
    String columnNames[] = {"Holding_No", "House_Name", "Owner_Name", "Mobile_No", "Email", "Floor_No", "Room_No", "Fare"};
    DefaultTableModel Model = new DefaultTableModel(columnNames,0);

    String query = "select * from rent_info";
    try{
        ResultSet rs = DBUTILS.queryExecute(query);
        while(rs.next()){
            String holdingno = rs.getString("Holding_No");
            String hname = rs.getString("House_Name");
            String oname = rs.getString("Owner_Name");
            String mbno = rs.getString("Mobile_No");
            String email = rs.getString("Email");
            String floor = rs.getString("Floor_No");
            String room = rs.getString("Room_No");
            String fare = rs.getString("Fare");
            String data[] = {holdingno,hname,oname,mbno,email,floor,room,fare};
            Model.addRow(data);
        }
        Table.setModel(Model);
    } 
    catch(Exception e){  
        e.printStackTrace();
     }
 }

